<?php
// このファイルはphp sample4.phpで実行できます

// PHPでは関数の引数・戻り値に「型」を指定できます
// 型を指定すると、間違った値が渡されたときにエラーになるため、バグを防げます

// ========================================================
// 基本の型一覧
// ========================================================
// int    : 整数 (1, 2, -3)
// float  : 小数 (1.5, 3.14)
// string : 文字列 ("hello")
// bool   : 真偽値 (true, false)
// array  : 配列
// void   : 戻り値なし

// ========================================================
// 例1: 引数と戻り値に型を指定
// ========================================================
echo "add(5, 3)\n";
$result = add(5, 3);
echo "$result\n"; // 8

function add(int $x, int $y): int
{
    return $x + $y;
}

// ========================================================
// 例2: 文字列型
// ========================================================
echo "greet('田中')\n";
echo greet("田中"); // こんにちは、田中さん！

function greet(string $name): string
{
    return "こんにちは、" . $name . "さん！\n";
}

// ========================================================
// 例3: 戻り値なし(void)
// ========================================================
echo "showPrice(1500)\n";
showPrice(1500); // 価格: 1500円

function showPrice(int $price): void
{
    echo "価格: " . $price . "円\n";
    // void関数はreturnで値を返せない
}

// ========================================================
// 例4: 配列型と真偽値型
// ========================================================
echo "isEmpty([])\n";
$empty = isEmpty(array());
echo $empty ? "空です\n" : "空ではありません\n"; // 空です

function isEmpty(array $items): bool
{
    return count($items) === 0;
}

// ========================================================
// 例5: strict_types（厳密な型チェック）
// ========================================================
// ファイルの先頭に declare(strict_types=1); を書くと
// 型が一致しない場合にエラーになります
//
// strict_typesなし（デフォルト）:
//   add("5", "3"); → 8 (文字列が自動的に数値に変換される)
//
// strict_typesあり:
//   add("5", "3"); → TypeError! (文字列は整数ではないのでエラー)
//
// 実際に試したい場合は以下のコメントを外して実行してみてください
// ※ strict_typesはファイルの先頭でのみ宣言できるため、このファイルでは動作しません
// ※ 試す場合はstudy.phpの先頭に declare(strict_types=1); を書いて試してください

echo "\n========================================================\n";
echo "型なしと型ありの比較\n";
echo "========================================================\n";

// 型なし（sample3.phpの書き方）
function oldAdd($x, $y)
{
    return $x + $y;
}

// 型あり（現代的な書き方）
function newAdd(int $x, int $y): int
{
    return $x + $y;
}

// どちらも同じ結果になるが、型ありの方が安全
echo "oldAdd(5, 3) = " . oldAdd(5, 3) . "\n";
echo "newAdd(5, 3) = " . newAdd(5, 3) . "\n";

// 型なしだと文字列を渡しても動いてしまう（意図しない動作の原因になる）
echo "oldAdd('5', '3') = " . oldAdd("5", "3") . "\n";
echo "↑ 動くが、本当に文字列を足したかったのか分からない\n";
